import Foundation // Required for the `Date` type

let myAge = 20 // Your age
let yourAge = 20 // Your friend's age

if myAge > yourAge {
    print("I'm older than you")
} else if myAge < yourAge {
    print("I'm younger than you")
} else {
    print("We're the same age")
}

let myMotherAge = myAge + 20 // Your mother's age
print("My mother age is \(myMotherAge)")

// Double my age
let doubleMyAge = myAge * 2
print("Double my age is \(doubleMyAge)")

